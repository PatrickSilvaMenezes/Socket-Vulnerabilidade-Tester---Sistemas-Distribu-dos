    private void btStartMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btStartMouseReleased
        
            client.ConexaoServidor();
            
            if(btPortScan.isEnabled()==false){
                if(txtIpTarget.getText().length()!=0)
                    client.enviarDadosPortScan(txtIpTarget.getText());
            }
            else if(btDos.isEnabled()==false){
                if(txtIpTarget.getText().length()!=0 && txtPortTarget.getText().length()!=0)
                    client.enviarDadosDos(txtIpTarget.getText(),txtPortTarget.getText());
            }
            btStart.setEnabled(false);
            
        
    }//GEN-LAST:event_btStartMouseReleased

